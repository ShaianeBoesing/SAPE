        <h2> Página da Jornada </h2>

        <p> Ao cadastrar-se no sistema, a primeira página encontrada é a Página de Jornada do Estudante. Nesta página estará um texto explicando o funcionamento da Jornada SAPE e um botão "Crie sua Jornada" para que o estudante cadastre a sua.</p>

        <img src="images/img25.png" class="img">

        <h2> Criar Jornada </h2>

        <p>Ao clicar em Crie sua Jornada, o estudante é encaminhado para a página de cadastro de jornada. Nesta página encontra-se um formulário onde é necessário informar as áreas que deseja incluir na Jornada, quantas questões quer em cada roteiro e quantas vezes na semana quer responder estes roteiros. Em seguida, deve-se clicar em "Criar".</p>

        <img src="images/img26.png" class="img">


        <h2> Visualizar Dados da Jornada </h2>

        <p>Após cadastrada, os dados selecionados podem ser visualizados pelo estudante. Além disso, o sistema mostrará uma data aproximada para o final da Jornada.</p>

        <img src="images/img27.png" class="img">


        <h2> Roteiros </h2>

        <p>Assim que a jornada for criada, os roteiros serão automaticamente gerados.</p>

        <img src="images/img28.png" class="img">

        <h2> Responder Roteiro </h2>

        <p>Para responder um roteiro, basta clicar no botao "Responder".</p>

        <img src="images/img29.png" class="img">

        <p>Ao clicar em "Responder", uma tela com todas as questões deste roteiro será aberta. Deve-se, portanto, selecionar a alternativa que considerar correta de cada uma das questões. É necessário responder todas as questões, mesmo que não se tenha certeza do resultado. Após responder todas as questões, deve-se clicar em "Conferir", assim, uma página com o feedback das questões será aberta.</p>

        <h2> Feedback do roteiro </h2>

        <p>Ao clicar em conferir, o feedback do roteiro será dado. Neste feedback o estudante poderá ver se acertou ou errou, qual a alternativa correta, um comentário sobre a questão (resolução), onde esta categorizada (em qual conteúdo e tópico ela se encaixa) e, por fim, alguns links de recomendação para se informar mais sobre o conteúdo e o tópico das questões. Para sair do feedback, deve-se clicar em "Voltar".</p>


        <h2> Histórico de roteiros </h2>

        <p>Ao clicar em "Voltar", o usuário será enviado para a página de histórico, onde poderá ver todos os roteiros já respondido, assim como abrir o feedback destes roteiros para caso queira visualizar novamento. Para visualizar um feedback antigo, basta clicar no botão "Ver".</p>

        <img src="images/img30.png" class="img">


        <h2> Desempenho do estudante </h2>

        <p>Assim que tiver respondido um roteiro, o desempenho já estará sendo calculado. Para visualizá-lo basta clicar em "Desempenho", na barra de menu superior da página de roteiros.</p>

        <img src="images/img31.png" class="img">

        <p>Na página do desempenho, o estudante poderá analisar qual o seu desempenho em cada uma das áreas (em porcentagem) e, além disso, poderá visualizar, através de um gráfico, seu desempenho geral, quantas questões foram respondidas ao todo, quantas foram acertadas e quantas foram erradas.</p>

        <img src="images/img32.png" class="img">

        <h2> Visualizar dados do estudante </h2>

        <p>Para visualizar os dados do estudante, este deve ir na barra lateral e clicar sobre o seu nome. Assim, será encaminhado para a página do estudante, onde esterão todos os dados informados no cadastro. </p>

        <img src="images/img33.png" class="img">

        <h2> Editar dados do estudante </h2>

        <p>Para editar os dados do estudante, este deve clicar no botão "Editar", na página do estudante.</p>

        <img src="images/img34.png" class="img">

        <p>Em seguida, deve alterar os dados que deseja e clicar e salvar.</p>

        <img src="images/img35.png" class="img">


        <h2> Sair do sistema </h2>

        <p>Para sair do sistema, deve-se clicar em "Sair", na barra de menu da página do estudante. </p>

        <img src="images/img36.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">



