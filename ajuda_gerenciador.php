        <h2> Página Inicial do Gerenciador </h2>

        <p>Com uma interface simples, o SAPE para gerenciadores contém especialmente os botões para que sejam realizados os cadastros de provas, áreas, matérias, conteúdos, tópicos e questões. Além disso, é possível visualizar todos os estudantes cadastrados no sistema.</p>
        <p> Ao acessar o sistema, a primeira página encontrada é a Página Inicial do Gerenciador. Nesta página estarão listadas as opções de cadastros de prova, área, matéria, conteúdo, tópico, questão e estudante. Cada item conta com um botão para adicionar um novo cadastro (representado pelo botão com sinal “+”) e um botão para visualizar os cadastros já realizados (representados pelo botão com o desenho de uma lupa). A exceção é o item “Estudante”, onde a única possibilidade é visualizar.</p>

        <img src="images/img1.png" alt="Tela inicial do sistema com destaque para os botões de adicionar novo cadastro e visualizar cadastrados" class="img">

        <h2> Cadastrar prova </h2>

        <p>Ao clicar em adicionar nova prova, o gerenciador é encaminhado para a página de cadastro de prova. Nesta página encontra-se um formulário onde é necessário informar o Nome da prova (exemplo: Exame Nacional do Ensino Médio), a sigla da prova (exemplo: ENEM) e o ano da prova (exemplo: 2020). E, por fim, clicar no botão “Cadastrar”.</p>

        <img src="images/img2.png" alt="Tela de cadastro de prova" class="img">


        <h2> Cadastrar área </h2>

        <p>Ao clicar em adicionar nova área, o gerenciador é encaminhado para a página de cadastro de área. Nesta página encontra-se um formulário onde é necessário informar o Nome da área (exemplo: Linguagens) e clicar no botão “Cadastrar”.</p>

        <img src="images/img3.png" alt="Tela de cadastro de área" class="img">


        <h2> Cadastrar matéria </h2>

        <p>Ao clicar em adicionar nova matéria, o gerenciador é encaminhado para a página de cadastro de matéria. Nesta página encontra-se um formulário onde é necessário informar o Nome da matéria (exemplo: Língua Portuguesa), selecionar a qual área pertence esta matéria (exemplo: Linguagens). E, por fim, clicar no botão “Cadastrar”.</p>

        <img src="images/img4.png" alt="Tela de cadastro de matéria" class="img">


        <h2> Cadastrar conteúdo </h2>

        <p>Ao clicar em adicionar novo conteúdo, o gerenciador é encaminhado para a página de cadastro de conteúdo. Nesta página encontra-se um formulário onde é necessário informar o Nome do conteúdo (exemplo: Variação Linguística), selecionar a qual matéria este conteúdo pertence (exemplo: Língua Portuguesa) e indicar um link de recomendação para mais informações sobre este conteúdo. E, por fim, clicar no botão “Cadastrar”.</p>

        <img src="images/img5.png" alt="Tela de cadastro de conteúdo" class="img">


        <h2> Cadastrar tópico </h2>

        <p>Ao clicar em adicionar novo tópico, o gerenciador é encaminhado para a página de cadastro de tópico. Nesta página encontra-se um formulário onde é necessário informar o Nome do tópico (exemplo: Língua VS Linguagem), selecionar a qual conteúdo este tópico pertence (exemplo: Variação Linguística) e indicar um link de recomendação para mais informações sobre este tópico. E, por fim, clicar no botão “Cadastrar”.</p>

        <img src="images/img6.png" alt="Tela de cadastro de tópico" class="img">


        <h2> Cadastrar questão </h2>

        <p>Ao clicar em adicionar nova questão, o gerenciador é encaminhado para a página de cadastro de questões (Figura 4). Nesta página encontra-se um formulário onde é necessário informar o ano da questão (na lista suspensa "Prova"), áre, matéria, conteúdo, tópico, enunciado, alternativas (de A a E), alternativa correta e resolução da questão.Por fim, deve-se clicar no botão “Cadastrar”.</p>

        <img src="images/img7.png" alt="Tela de cadastro de questão - parte 1" class="img">
        <img src="images/img8.png" alt="Tela de cadastro de questão - parte 2" class="img">


        <h2> Visualizar Cadastros </h2>

        <p>Ao clicar no botão visualizar, representado por uma lupa, o gerenciador é encaminhado para a página de visualização de cadastros, a qual conta com estrutura semelhante para todos os cadastro. Nesta página encontram-se todos os dados cadastrados organizados em estrutura de tabela.</p>

        <img src="images/img9.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <h2> Buscar Cadastro </h2>

        <p>Ainda na tela visualizar, é possível encontrar algum cadastro com mais facilidade através da ferramenta de busca, onde poderá ser digitado parte do conteúdo do cadastro que se desaja encontrar e clicar em "Buscar". Em seguida, para retornar a visualização padrão de todos os cadastros, deve-se cliclar em "Voltar".</p>

        <h2> Buscar Cadastro de Prova </h2>

        <p>A primeira opção de busca é dos cadastros de prova. Nesta, as provas deverão ser pesquisadas com base em seus anos de aplicação (exemplo: 2012). Caso deseje-se visualizar todas as provas que encontram-se entre os anos 2010 e 2019, deve-se digitar "201", assim, todas as provas que iniciam com os números 201 serão encontradas. Além disso, há a opção de buscar através do campo "id".</p>

        <img src="images/img10.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <h2> Buscar Cadastro de Área </h2>

        <p>Em seguida, há a opção de busca dos cadastros de área. Nesta página, as áreas deverão ser pesquisadas com base em seus nomes (exemplo: Linguagens). Além disso, há a possibilidade de buscar através do campo "id"</p>

        <img src="images/img12.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">


        <h2> Buscar Cadastro de Matéria </h2>

        <p>Em seguida, há a opção de busca dos cadastros de matéria. Nesta página, as matérias deverão ser pesquisadas com base em seus nomes (exemplo: Biologia),ou ainda, através da área do conhecimento a qual pertence. Além disso, há a possibilidade de buscar através do campo "id"</p>

        <img src="images/img13.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">


        <h2> Buscar Cadastro de Conteúdo </h2>

        <p>Em seguida, há a opção de busca dos cadastros de conteúdo. Nesta página, os conteúdos deverão ser pesquisadas com base em seus nomes (exemplo: Variação Linguística),ou ainda, através da matéria a qual pertence. Além disso, há a possibilidade de buscar através do campo "id"</p>

        <img src="images/img15.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <h2> Buscar Cadastro de Tópico </h2>

        <p>Em seguida, há a opção de busca dos cadastros de tópico. Nesta página, os tópicos deverão ser pesquisadas com base em seus nomes (exemplo: língua),ou ainda, através da matéria a qual pertence. Além disso, há a possibilidade de buscar através do campo "id"</p>

        <img src="images/img14.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <h2> Buscar Cadastro de Questão </h2>

        <p>Em seguida, há a opção de busca dos cadastros de questões. Nesta página, as questões poderão ser pesquisadas com base em seus enunciados, ano da prova, área, matéria, conteúdo ou tópico a qual pertencem. Além disso, há a possibilidade de buscar através do campo "id".</p>

        <img src="images/img16.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">


        <h2> Buscar Cadastro de Estudantes </h2>

        <p>Em seguida, há a opção de busca dos cadastros de estudantes. Nesta página, os estudantes poderão ser pesquisadas com base em seus nomes, sobrenomes ou endereços de e-mail. Além disso, há a possibilidade de buscar através do campo "id". Note que o cadastro de estudante não é editável ou excluível. Apenas o próprio estudante tem o poder de fazer isto.</p>

        <img src="images/img18.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <h2> Botão de editar </h2>

        <p>Em todas as páginas de visualizar, com exceção da página de visualizar estudantes, haverá o botão "Editar" para cada um dos cadastros feitos. </p>

        <img src="images/img19.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <p> Ao clicar neste botão, um formulário igual ao formulário de cadastro abrirá e ali poderão ser feitas as alterações necessárias. Para salvar estas alterações, deve-se clicar no botão "Salvar".</p>

        <img src="images/img20.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">

        <h2> Botão de excluir </h2>

        <p>Em todas as páginas de visualizar, com exceção da página de visualizar estudantes, haverá o botão "Excluir" para cada um dos cadastros feitos. Assim, deve-se selecionar todos os cadastros que deseja excluir e, então, clicar no botão superior "Excluir Cadastros Selecionados".</p>

        <img src="images/img21.png" alt="Tela de visualização de cadastros (Tela de visualizar áreas usada como exemplo)" class="img">


